const fs = require('fs');
const path = require('path');

const METRICS_DIR = path.join(process.cwd(), '.heal_metrics');
const finalSummaryPath = path.join(METRICS_DIR, 'final_run_summary.json');

if (!fs.existsSync(finalSummaryPath)) {
  process.exit(0);
}

const data = JSON.parse(fs.readFileSync(finalSummaryPath, 'utf-8'));
const iterations = data.iterations.filter(Boolean);

if (iterations.length === 0) {
  process.exit(0);
}

let totalAiCalls = 0;
let maxIssuesAtStart = 0;
let issuesAtEnd = 0;
const filesTouched = new Set();
let totalConfidence = 0;

iterations.forEach((iter, idx) => {
  if (idx === 0) maxIssuesAtStart = iter.issuesBefore;
  issuesAtEnd = iter.issuesAfter;

  totalAiCalls += iter.aiCalls || 0;
  totalConfidence += iter.successScore || 0;

  Object.keys(iter.filesTargeted || {}).forEach((file) => filesTouched.add(file));
});

const delta = maxIssuesAtStart - issuesAtEnd;

const prMarkdown = `
## Repo Heal Repair Report

Repo Heal detected formatting, accessibility, or syntactical issues in this branch and attempted automated repairs.

### Summary Metrics
- **Initial Issues Detected:** ${maxIssuesAtStart}
- **Issues Remaining:** ${issuesAtEnd}
- **Total Bugs Fixed:** ${delta > 0 ? delta : 0}
- **Confidence Score:** +${totalConfidence}

### Execution Details
- **Iterations Ran:** ${iterations.length}
- **LLM Agent Calls Hosted:** ${totalAiCalls}
- **Files Modified:** ${filesTouched.size}

<details>
<summary><b>View Modified Files</b></summary>

${Array.from(filesTouched)
  .map((file) => `- \`${file}\``)
  .join('\n')}
</details>

> *This PR was generated by Repo Heal using Gemini-assisted repair.*
`;

